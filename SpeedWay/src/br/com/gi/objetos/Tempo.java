package br.com.gi.objetos;


public class Tempo {
	private int temperatura;
	private int qtdAguaPista;
	
	public Tempo(){
		
	}
	public int getTemperatura() {
		return temperatura;
	}
	public void setTemperatura(int temperatura) {
		this.temperatura = temperatura;
	}
	public int getQtdAguaPista() {
		return qtdAguaPista;
	}
	public void setQtdAguaPista(int qtdAguaPista) {
		this.qtdAguaPista = qtdAguaPista;
	}
}
